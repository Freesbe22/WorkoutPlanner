namespace WorkoutPlanner
{
    public enum WorkoutSection
    {
        Warmup,           // Échauffement
        StrengthTraining, // Musculation (classique)
        Cardio,           // Cardio continu (ex: course, vélo)
        HIIT,             // Entraînement par intervalles de haute intensité
        Tabata,           // Tabata (8x 20s effort / 10s repos)
        Circuit,          // Circuit Training (enchaînement d’exercices)
        Stretching,       // Étirements (actifs/passifs)
        Cooldown,         // Retour au calme (fin de séance)
        Rest              // Repos (peut être utilisé pour les jours de repos)
    }

}